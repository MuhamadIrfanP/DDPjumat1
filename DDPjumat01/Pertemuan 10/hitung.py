import math

def tambah (a1, a2):
    hitung = a1 + a2
    print(f'hasil dari {a1} + {a2} = {hitung}')
    
def kurang (a1, a2):
    hitung = a1 - a2
    print(f'hasil dari {a1} - {a2} = {hitung}')
    
def kali (a1, a2):
    hitung = a1 * a2
    print(f'hasil dari {a1} x {a2} = {hitung}')
    
def bagi (a1, a2):
    hitung = a1 / a2
    print (f'hasil dari {a1} : {a2} = {hitung}')
    
def pangkat(a1, p):
    hitung = a1 ** p
    print (f'hasil dari {a1}^{p} = {hitung}')